def zs_dump_list(stock_code_list):
    selects = []
    total_amo = 0

    # 
    today_list = GetSaveStock.get_current_batch(stock_code_list, False)
    for num in range(0, len(stock_code_list)):
        selects.append(today_list[num])
        total_amo += today_list[num]['amo']

